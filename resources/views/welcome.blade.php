<h5>B.   KEWAJIBAN SISWA</h5>
<h6>1.   Masuk Sekolah.</h6>
  -Jam masuk sekolah pukul 07.00, Siswa harus berada di dalam kelas.<br>
<h6>2.   Ketentuan Seragam</h6>
<h6>2.1. Umum.</h6>
  -Senin – Kamis : Putih – abu-abu, berdasi abu-abu, Sepatu hitam, kaos kaki putih, bukan sepatu ballet.<br>
  -Jum’at – Sabtu : Pramuka, sepatu hitam, kaos kaki hitam, bukan sepatu ballet.<br>
  -Model pakaian /celana sesuai dengan petunjuk sekolah,pakaian olahraga dari sekolah.<br>
  -Seragam praktek sesuai dengan ketentuan sekolah.<br>
  -Memakai topi sekolah sesuai ketentuan dan ikat pinggang warna hitam.<br>
  -Pakaian tidak terbuat dari bahan yang tipis dan tembus pandang, tidak ketat dan tidak membentuk tubuh.<br>
  -Tidak memakai jaket, kecuali sakit.<br>
  -Tidak menulisi pakaian atau memakai aksesoris selain yang ditentukan.<br>
  -Tidak mengenakan perhiasan yang berlebihan atau mencolok.<br>
<h6>2.2. Khusus Laki-laki</h6>
  -Baju dimasukkan kedalam celana.<br>
  -Panjang celana sesuai ketentuan.<br>
  -Celana dan lengan baju tidak digulung.<br>
  -Celana tidak dimodifikasi, disobek atau dijahit cutbrai.<br>
<h6>2.3. Khusus Perempuan</h6>
  -Baju dimasukkan kedalam rok.<br>
  -Panjang rok sampai mata kaki.<br>
  -Bagi yang berjilbab : Baju dimasukkan kedalam rok , panjang rok sampai mata kaki dan jilbab berwarna putih untuk baju putih serta jilbab berwarna coklat tua untuk pakaian pramuka (tidak jilbab gaul).<br>
<h5>3.  Upacara Bendera</h5>
  -Setiap siswa wajib mengikuti upacara yang diselenggarakan sekolah.<br>
  -Siswa wajib memakai seragam lengkap termasuk topi sewaktu mengikuti upacara.
<h5>4.  Rambut, Kuku, Tato, Make up</h5>
<h6>4.1. Umum</h6>
    Siswa dilarang : <br>
  -Berkuku panjang.<br>
  -Mengecat rambut dan kuku.<br>
  -Bertato.<br>
 <h6>4.2. Khusus Siswa Laki-laki</h6>
  -Rambut disisir rapi.<br>
  -Tidak berambut panjang, bercukur gundul, atau dikuncir.<br>
  -Tidak memakai kalung, anting atau gelang.<br>
  -Tidak berkumis, berjenggot atau mengecat rambut.<br>
<h6>4.3.  Khusus Siswa Perempuan</h6>
  -Rambut disisir rapi, diikat atau dikepang untuk yang berambut panjang.<br>
  -Tidak memakai make up atau sejenisnya, yang berlebihan.<br>
<h5>5.   Masuk dan Pulang Sekolah</h5>
  -Siswa wajib hadir 5 menit sebelum bel sekolah berbunyi.<br>
  -Siswa terlambat datang kurang dari 15 menit harus lapor kepada guru piket dan diizinkan masuk kelas.<br>
  -Siswa terlambat datang lebih dari 15 menit harus lapor kepada guru piket dan tidak diizinkan masuk kelas pada pelajaran pertama, kecuali ulangan.<br>
  -Selama pelajaran berlangsung atau pada pergantian pelajaran siswa dilarang berada di luar kelas.<br>
  -Pada waktu pulang sekolah siswa diwajibkan langsung pulang ke rumah masing-masing, kecuali mengikuti kegiatan ekstrakurikuler.
  <br>
  -Pada waktu pulang sekolah siswa dilarang duduk-duduk (nongkrong) di tepi jalan atau tempat tertentu.<br>

<h5>6.  Larangan – larangan</h5>
    Dalam kegiatan sehari-hari di lingkungan sekolah siswa dilarang melakukan hal-hal sebagai berikut:<br>
  -Membawa, mengedarkan, mengkonsumsi rokok, minuman keras, narkoba dan obat terlarang lainnya.<br>
  -Berpacaran.<br>
  -Berkelahi atau melibatkan diri dalam perkelahian di lingkungan sekolah atau di luar sekolah.<br>
  -Membuang sampah tidak pada tempatnya.<br>
  -Mencoret-coret dinding sekolah atau fasilitas umum lainnya.<br>
  -Berbicara kotor, mengumpat, bergunjing, menghina, atau menyapa sesama siswa atau warga sekolah dengan kata-kata yang tidak senonoh.<br>
  -Membawa senjata tajam, senjata api atau alat-alat lain yang membahayakan.<br>
  -Menggunakan telepon seluler (HP) pada jam pelajaran atau ujian.<br>
  -Membawa, membaca, atau mengedarkan bacaan, gambar, sketsa, audio atau video pornografi.<br>
  -Membawa alat-alat yang dipakai utuk berjudi.</p>
   
 
